CREATE DATABASE avaliacao;

USE avaliacao;

CREATE TABLE produto(
ID INT AUTO_INCREMENT ,
NOME VARCHAR(80),
DESCRICAO VARCHAR(150),
QUANTIDADE INT,
PRECO FLOAT,
PRIMARY KEY (ID));

CREATE TABLE filme(
ID INT AUTO_INCREMENT ,
NOME VARCHAR(80),
DESCRICAO VARCHAR(150),
ano date,
PRIMARY KEY (ID));


